        <div>
          角膜矯形術（Ortho-K，俗稱OK鏡）是一項非手術矯正視力方法，透過配戴特製的高透氧度硬性隱形眼鏡（角膜矯形鏡），以壓力慢慢改變角膜弧度，使角膜中央部分變得平坦，令外界光線重新聚焦到視網膜上，看見清晰的影像。由於角膜富有彈性，弧度改善並不持久，不能將近視徹底根治。
